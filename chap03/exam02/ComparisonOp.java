package exam02;

public class ComparisonOp { //비교(관계) 연산자

	public static void main(String[] args) {
		
		System.out.println(350 < 200);
		System.out.println(350 > 200);
		System.out.println(350 <= 200);
		System.out.println(350 >= 200);
		System.out.println(350 == 200);
		System.out.println(350 != 200);
		

	}

}
